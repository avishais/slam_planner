function UGV( q )

% q = [0; 0; 0*pi/4];

M = [-0.25 0.25 0.25 -0.25; 
    -0.18 -0.18 0.18 0.18]';
C = [0.10 0.25 0.25 0.10;
    -0.07 -0.07 0.07 0.07]';

R = [cos(q(3)) -sin(q(3)); sin(q(3)) cos(q(3))];

M = (R*M')' + repmat(q(1:2)',4,1);
C = (R*C')' + repmat(q(1:2)',4,1);

patch(M(:,1), M(:,2),'b');
patch(C(:,1), C(:,2),'r');


end

